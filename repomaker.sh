function select_license() {
  printf "Software licenses:\n"
  printf "\t0. BSD2-Clause\n"
  printf "\t1. MIT\n"
  printf "\t2. GPLv2\n"
  printf "\t3. GPLv3\n"

  printf "Select software license: "; read ilicense
  case "$ilicense" in 
    0)
      license="$LICENSE_BSD2CLAUSE"
      ;;
    1)
      license="$LICENSE_MIT"
      ;;
    2)
      license="$LICENSE_GPL2"
      ;;
    3)
      license="$LICENSE_GPL3"
      ;;
    *)
      echo "Unknown license!"; exit 1
      ;;
  esac
}

function license_decode() {
  echo "$1" | base64 -d | gzip -d -c -n
}

function license_template() {
  sed -i -e "s/$2/$3/" "$1"
}

function repomaker_genlicense() {
  license_file=$(mktemp)
  license_decode "$license" > "$license_file"
  
  license_template "$license_file" "COPYRIGHT_YEAR" "$(date +%Y)"
  license_template "$license_file" "COPYRIGHT_HOLDER" "$COPYRIGHT_HOLDER"

  output=$(cat "$license_file")
  rm "$license_file"
  echo "$output"
}

function repomaker_init() {
  printf "Enter repo name: "; read repo_name
  printf "Enter repo description: "; read repo_desc

  echo "Remotes: "
  for i in "${!REMOTES[@]}"; do
    printf "\t%d. %s\n" $i "${REMOTES[$i]}"
  done

  printf "Select primary remote: "; read iremote_primary
  remote_primary="${REMOTES[$iremote_primary]}"

  # create repo directory
  repo_path="$DEV_PATH/$repo_name"
  mkdir -p "$repo_path"
  # generate license
  select_license
  repomaker_genlicense > "$repo_path/LICENSE"
  # add readme
  printf "# $repo_name\n\n$repo_desc\n" > "$repo_path/README.md"
  # init git repo
  cd "$repo_path"
  git init
  # add primary remote
  git remote add origin $(echo "$remote_primary" | sed "s/REPO_NAME/$repo_name/")
  # add push remotes
  for i in "${!REMOTES[@]}"; do
    if [ "$i" -ne "$iremote_primary" ]; then
      git remote set-url --add origin $(echo "${REMOTES[$i]}" | sed "s/REPO_NAME/$repo_name/")
    fi
  done
  # all done!
}


case "$1" in
  "init")
    repomaker_init
    ;;
  "genlicense")
    select_license
    repomaker_genlicense
    ;;
  *)
    echo "repomaker.sh init/genlicense"; exit 1
esac
